  JuMP.@variable(m, SPOT_IMP_USH_NOW[i = 1:numTimeBlock]>=0)
  JuMP.@variable(m, WATER_VALUE >= 0)
  JuMP.@variable(m, LAMBDA >= 0)
  JuMP.@variable(m, INFLOW_NT_BC >= 0)
  JuMP.@variable(m, INFLOW_NT_US >= 0)
  JuMP.@variable(m, INFLOW_TY_MCA >= 0)
  JuMP.@variable(m, INFLOW_TY_ARD >= 0)
  JuMP.@variable(m, INFLOW_FLEX_MCA >= 0)
  JuMP.@variable(m, INFLOW_FLEX_ARD >= 0)
