  JuMP.@variable(m, Q_ARD_TY_FLEX)
  JuMP.@variable(m, Q_MCA_TY_NTBC)
  JuMP.@variable(m, Q_MCA_TY_NTUS)
  JuMP.@variable(m, Q_MCA_NTBC_NTUS)
  JuMP.@variable(m, Q_ARD_TY_LCA)
  JuMP.@variable(m, Q_ARD_TY_IM)
  JuMP.@variable(m, Q_ARD_LCA_IM)
  JuMP.@variable(m, DEFICITIMP[i = 1:numTimeBlock] >=0)
