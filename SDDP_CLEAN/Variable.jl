  JuMP.@variable(m, V_FLEX_ARD_NOW)
  JuMP.@variable(m, V_FLEX_ARD_SV)
  JuMP.@variable(m, Q_FLEX_ARD)
  JuMP.@variable(m, Q_SOA)
  JuMP.@variable(m, V_SOA_SOM)
  JuMP.@variable(m, Q_LCA)
  JuMP.@variable(m, V_LCA_NOW)
  JuMP.@variable(m, V_LCA_SOM)
  JuMP.@variable(m, V_TY_MCA_NOW)
  JuMP.@variable(m, Q_TY_MCA_NOW)
  JuMP.@variable(m, V_TY_MCA_SV)
  JuMP.@variable(m, V_TY_ARD_NOW)
  JuMP.@variable(m, Q_TY_ARD_NOW)
  JuMP.@variable(m, V_TY_ARD_SV)
  JuMP.@variable(m, V_NT_BC_NOW)
  JuMP.@variable(m, V_NT_US_NOW)
